# Python supports a variety of operations. Here are a few. 

a = 1
b = 2

# arithmetic
print("here, we are trying arithmetic operators")
print(a+b)
print(a-b)
print(a*b)
print(a/b) # by default, this is real number division. 1/2 gives us 0.5
print(a**b)
print(10%3) # integer division, result is remainder (modulo)

# logical operations: these yield a boolean (true/false) output.
print("here, we are trying logical operators")
print(a<b)
print(a>b)
print(a==b) # note the double equal sign. single equal sign is only for assigning values to variables
print(a>=b)
print(a<=b)
print(a!=b)